package com.roytuts.java.fork.join;

public class Employee {

	private String name;
	private float halfYear;

	public Employee(String name, float halfYear) {
		this.name = name;
		this.halfYear = halfYear;
	}

	public String getName() {
		return name;
	}

	public float getHalfYear() {
		return halfYear;
	}

}
